function toggleMenu(){
    const menuToggle = document.querySelector('.toggle-menu-header');
    const navToggle = document.querySelector('.site-header-menu');
    menuToggle.classList.toggle('active')
    navToggle.classList.toggle('active')
}

function trailerModal(){
    const modalTrailer = document.querySelector('.re4-trailer-modal'); 
    modalTrailer.classList.toggle('active')   
}    










